package com.zjgt.p2p.report.model;


public class DataImpExample {
	
	private String year;
	private String month;
	private String ymdTime;
	private String isAllOrDaily;
	
	public String getYear()
	{
		return year;
	}
	public void setYear(String year)
	{
		this.year = year;
	}
	public String getMonth()
	{
		return month;
	}
	public void setMonth(String month)
	{
		this.month = month;
	}
	public String getYmdTime()
	{
		return ymdTime;
	}
	public void setYmdTime(String ymdTime)
	{
		this.ymdTime = ymdTime;
	}
	public String getIsAllOrDaily()
	{
		return isAllOrDaily;
	}
	public void setIsAllOrDaily(String isAllOrDaily)
	{
		this.isAllOrDaily = isAllOrDaily;
	}
	
    
}